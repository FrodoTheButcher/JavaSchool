public class Teme {
    private String name;
    private int punctaj;

    public Teme(String name, int punctaj) {
        this.name = name;
        this.punctaj = punctaj;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPunctaj() {
        return punctaj;
    }

    public void setPunctaj(int punctaj) {
        this.punctaj = punctaj;
    }
}
